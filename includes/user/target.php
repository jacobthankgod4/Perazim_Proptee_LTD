<script>
        function toggleContent(type) {
            // Get toggle buttons and content sections
            const buttons = document.querySelectorAll(".toggle-btn");
            const contents = document.querySelectorAll(".content");

            // Remove active state from all buttons and contents
            buttons.forEach(btn => btn.classList.remove("active"));
            contents.forEach(content => content.classList.remove("active"));

            // Add active state to the selected button and content
            document.querySelector(`[onclick="toggleContent('${type}')"]`).classList.add("active");
            document.getElementById(`${type}-content`).classList.add("active");
        }
    </script>